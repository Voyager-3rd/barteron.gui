class BanProcessor {

	constructor() {
		const env = process.env.NODE_ENV;

		this.isEnabled = banLists.isEnabled[env];

		this.banList = {};
		if (this.isEnabled) {
			this.banList = (banLists.data[env] || '')
				.split("\n")
				.map(m => m.trim())
				.filter(f => f)
				.reduce((acc,value) => {
					acc[value] = true;
					return acc;
				}, {});
		};
	}

	filteredOffers(items) {
		let result = items;
		if (Array.isArray(items)) {
			const banList = this.banList;
			result = items.filter(f => f?.address ? !(banList[f.address]) : true);
		}
		return result;
	}

	isBannedAddress(address) {
		const key = address && String(address);
		return key ? this.banList[key] : false;
	}
}

const banProcessor = new BanProcessor();

export default banProcessor;
